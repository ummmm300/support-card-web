function distributeExamParams({ fixed, variable, ratio }) {
    const vo = Number(ratio?.vo ?? 0);
    const da = Number(ratio?.da ?? 0);
    const vi = Number(ratio?.vi ?? 0);
    const totalRatio = vo + da + vi;

    if (totalRatio <= 0) {
        return {
            vo: fixed,
            da: fixed,
            vi: fixed,
        };
    }

    return {
        vo: fixed + Math.floor((variable * vo) / totalRatio),
        da: fixed + Math.floor((variable * da) / totalRatio),
        vi: fixed + Math.floor((variable * vi) / totalRatio),
    };
}

export function calculateHifExamParams(ratio) {
    return HIF_EXAM_PARAM_RULES.reduce(
        (total, rule) => {
            const params = distributeExamParams({
                fixed: rule.fixed,
                variable: rule.variable,
                ratio,
            });

            return {
                vo: total.vo + params.vo,
                da: total.da + params.da,
                vi: total.vi + params.vi,
            };
        },
        { vo: 0, da: 0, vi: 0 }
    );
}

export function applyHifExamParamsToContext({
    context,
    type,
    examRatioPresetKey,
    manualRatio,
}) {
    const preset =
        HIF_EXAM_RATIO_PRESETS[examRatioPresetKey] ??
        HIF_EXAM_RATIO_PRESETS.trendPair;

    const ratio =
        examRatioPresetKey === "manual"
            ? manualRatio
            : preset?.ratiosByType?.[type];

    const examParams = calculateHifExamParams(
        ratio ?? { vo: 4, da: 4, vi: 1 }
    );

    return {
        ...context,
        param_vo_total: (context.param_vo_total ?? 0) + examParams.vo,
        param_da_total: (context.param_da_total ?? 0) + examParams.da,
        param_vi_total: (context.param_vi_total ?? 0) + examParams.vi,
    };
}